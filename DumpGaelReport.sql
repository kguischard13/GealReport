 FOR EACH ROW begin
update stats
set Num_Games = num_Games -1
where stats.sportID = old.sportID;

update stats
set home_wins = home_wins - 1 
where stats.sportID = old.sportID;

update stats
set away_wins = away_wins - 1 
where stats.sportID = old.sportID;

update stats
set win_loss_percent = ((away_wins + home_wins)/num_games) *100
where stats.sportID = old.sportID;
end
