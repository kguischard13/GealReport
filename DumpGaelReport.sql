CREATE TRIGGER `numofGames` AFTER INSERT ON `schedule`
 FOR EACH ROW begin
update stats
set Num_Games = (select count(*) from schedule where sportID = new.sportID  
group by sportID)
where stats.sportID = new.sportID;

update stats
set Home_Wins = (
select count(*)
from schedule
where sportID = new.sportID and (location like '%Rochelle%' or 
location like 'Van Cor%') and Status like 'Win'
group by sportid)
where stats.sportID = new.sportID;

update stats
